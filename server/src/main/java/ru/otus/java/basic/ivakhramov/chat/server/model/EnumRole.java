package ru.otus.java.basic.ivakhramov.chat.server.model;

/**
 * EnumRole represents the different roles that can be assigned to users in the chat application.
 * <p>
 * The available roles are:
 * <ul>
 *     <li><b>ADMIN</b>: Represents an administrator role with elevated privileges.</li>
 *     <li><b>USER</b>: Represents a standard user role with basic permissions.</li>
 * </ul>
 * </p>
 */
public enum EnumRole {
    ADMIN,
    USER
}
